/**
 * 1. 了解需求
 *
 *    在 0/1 背包问题中，你有一组物品，每种物品都有自己的重量 (weight) 和价值 (value)。
 *    你有一个承重能力有限的背包 (capacity)。
 *
 *    约束：每种物品只有一件，要么装入（1），要么不装（0）
 *    目标：在不超过背包最大承重的前提下，使装入物品的总价值最大
 */

/**
 * 2. 解决方案概略：动态规划 (DP)
 *
 *    创建一个二维数组 dp 用于记录子问题的解：
 *    - 行：物品索引（0 到 n）
 *    - 列：背包容量（0 到 capacity）
 *
 *    dp[i][j] 含义：考虑前 i 个物品，在背包容量为 j 时能获得的最大价值
 *
 *    决策逻辑：
 *    - 不放第 i 个物品：dp[i-1][j]（前 i-1 个物品在容量 j 下的最大价值）
 *    - 放第 i 个物品（前提 j ≥ weights[i-1]）：
 *      value[i-1] + dp[i-1][j - weights[i-1]]
 *
 *    转移方程：
 *    dp[i][j] = max(dp[i-1][j], value[i-1] + dp[i-1][j - weight[i-1]])
 */

/**
 * 解决 0/1 背包问题
 * @param {number} capacity - 背包最大容量
 * @param {number[]} weights - 物品重量数组
 * @param {number[]} values - 物品价值数组
 * @returns {number} - 能够获得的最大总价值
 */
function knapsack01(capacity, weights, values) {
  const n = weights.length;

  // 1. 初始化 DP 矩阵
  // 创建一个 (n + 1) x (capacity + 1) 的二维数组，初始值填充为 0
  // dp[i][j] 表示前 i 个物品在容量为 j 时的最大价值
  const dp = Array.from({ length: n + 1 }, () => Array(capacity + 1).fill(0));

  // 2. 迭代填充 DP 表
  for (let i = 1; i <= n; i++) {
    const currentWeight = weights[i - 1]; // 当前物品重量
    const currentValue = values[i - 1];   // 当前物品价值

    for (let j = 0; j <= capacity; j++) {
      if (currentWeight <= j) {
        // 如果当前物品能装下，对比“装”与“不装”哪个价值更高
        dp[i][j] = Math.max(
          dp[i - 1][j],                         // 不装
          dp[i - 1][j - currentWeight] + currentValue // 装
        );
      } else {
        // 如果当前物品装不下，承袭前 i-1 个物品的最优解
        dp[i][j] = dp[i - 1][j];
      }
    }
  }

  // 3. 返回右下角的值，即为最终最优解
  return dp[n][capacity];
}

// 测试数据
const weights = [2, 3, 4, 5];
const values = [3, 4, 5, 6];
const capacity = 5;

console.log("最大价值为:", knapsack01(capacity, weights, values)); // 输出应该是 7 (选择重量 2 和 3 的物品)

/**
 * 动态规划循环逻辑详解 —— “填表游戏”
 * 每一格的答案都取决于它”正上方”或”左上方”已经填好的数字
 *
 * ─────────────────────────────────────────────────────────────
 * 1. 场景设定
 *    物品 1：重量 2，价值 3
 *    物品 2：重量 3，价值 4
 *    背包总容量：5
 *    → 创建一个 3x6 的表格（3 行：0, 1, 2 物品；6 列：0 到 5 容量）
 *
 * ─────────────────────────────────────────────────────────────
 * 2. 双层循环的”上帝视角”
 *
 *    for (let i = 1; i <= n; i++) {           // 外层：决定现在手里拿到了哪件物品
 *        for (let j = 0; j <= capacity; j++) { // 内层：尝试在不同大小的背包里装这件物品
 *            // 逻辑判断...
 *        }
 *    }
 *
 *    外层循环 i —— 物品的选择范围：
 *      - i = 1 时：手里只有物品 1，思考在容量 1-5 下能拿到的最大价值
 *      - i = 2 时：手里有物品 1 和 2，结合两件物品求容量 1-5 下的最优解
 *
 *    内层循环 j —— 模拟背包容量的变化：
 *      从容量 0 模拟到最大容量，大背包的最优解由小背包的最优解推导
 *
 * ─────────────────────────────────────────────────────────────
 * 3. 核心逻辑的”大白话”翻译
 *    面临抉择：”这件新物品，装还是不装？”
 *
 *    情况一：装不下 (currentWeight > j)
 *      - 逻辑：背包太小，放不进去
 *      - 动作：直接抄写上一行同容量的答案
 *      - 代码：dp[i][j] = dp[i-1][j]
 *
 *    情况二：装得下 (currentWeight <= j)
 *      - 选项 A（不装）：保持之前的最优解 dp[i-1][j]
 *      - 选项 B（装）：腾出空间放它，dp[i-1][j - currentWeight] + currentValue
 *      - 决策：取两者最大值
 *
 * ─────────────────────────────────────────────────────────────
 * 4. 模拟过程（以容量 5 为例）
 *    计算 i=2（物品 2，重 3，值 4），容量 j=5 时：
 *
 *    步骤一：不装物品 2
 *      - 看 dp[1][5]（只拿物品 1 时的价值）
 *      - 结果：3
 *
 *    步骤二：装物品 2
 *      - 物品 2 价值：4
 *      - 剩余空间：5 - 3 = 2
 *      - 看 dp[1][2]（容量为 2 时只拿物品 1 的价值）
 *      - 结果：3
 *      - 总价值：4 + 3 = 7
 *
 *    步骤三：取最大值
 *      - max(3, 7) = 7
 *      - 所以 dp[2][5] = 7
 */